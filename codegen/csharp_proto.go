func generateCSharpWorldProto(outDir, golemImport, namespace string, proto schema.ProtoTemplateData) error {
	var b strings.Builder
	writeCSharpHeader(&b, golemImport, namespace)
	for _, wt := range proto.WorldTypes {
		writeCSMessageClass(&b, wt.DataName, worldDataCSFields(wt))
	}
	writeCSWorldUpdateClass(&b, proto)
	writeCSharpFooter(&b, namespace)
	outPath := filepath.Join(outDir, "WorldPb.cs")
	if err := os.WriteFile(outPath, []byte(b.String()), 0o644); err != nil {
		return err
	}
	fmt.Printf("  wrote %s\n", outPath)
	return nil
}

func worldDataCSFields(wt schema.WorldTypeData) []msgField {
	var fields []msgField
	for _, f := range wt.Fields {
		fields = append(fields, worldFieldToCSMsgField(f))
	}
	return fields
}

func worldFieldToCSMsgField(f schema.WorldFieldInfo) msgField {
	return msgField{
		Name:            f.CSProtoField,
		ProtoType:       f.ProtoType,
		Tag:             f.ProtoTag,
		IsRepeated:      f.IsRepeated,
		IsMap:           f.IsMap,
		MapKeyProtoType: f.MapKeyProtoType,
		ElemProtoType:   f.ElemProtoType,
		ElemIsCustom:    f.ElemIsCustom,
		ElemTSType:      f.ElemCSType,
		MapKeyTSType:    f.MapKeyCSType,
	}
}

