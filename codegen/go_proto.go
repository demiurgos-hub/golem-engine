// writeGoClientPacket emits a transport envelope that batches multiple encoded
// ClientMessage blobs into one reliable message payload.
func writeGoClientPacket(b *strings.Builder) {
	w := func(f string, a ...any) { fmt.Fprintf(b, f+"\n", a...) }

	w("")
	w("// ClientPacket batches one or more encoded ClientMessage blobs.")
	w("type ClientPacket struct { Frames [][]byte }")
	w("")
	w("// Marshal encodes the client packet to protobuf wire format.")
	w("func (m *ClientPacket) Marshal() []byte {")
	w("\tw := &pb.Writer{}")
	w("\tfor _, frame := range m.Frames {")
	w("\t\tw.Tag(1, 2).Bytes(frame)")
	w("\t}")
	w("\treturn w.Finish()")
	w("}")
	w("")
	w("// Unmarshal decodes a client packet from protobuf wire format.")
	w("func (m *ClientPacket) Unmarshal(data []byte) (retErr error) {")
	w("\tdefer func() { if r := recover(); r != nil { retErr = fmt.Errorf(\"pb: decode ClientPacket: %%v\", r) } }()")
	w("\trd := pb.NewReader(data)")
	w("\tm.Frames = m.Frames[:0]")
	w("\tfor !rd.Done() {")
	w("\t\tfield, wire := rd.Tag()")
	w("\t\tswitch field {")
	w("\t\tcase 1:")
	w("\t\t\tframe := append([]byte(nil), rd.Bytes()...)")
	w("\t\t\tm.Frames = append(m.Frames, frame)")
	w("\t\tdefault:")
	w("\t\t\trd.Skip(wire)")
	w("\t\t}")
	w("\t}")
	w("\treturn nil")
	w("}")
}

