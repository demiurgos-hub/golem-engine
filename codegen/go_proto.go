// generateGoWorldProto writes world_pb.go into outDir with per-world-type
// structs (implementing world.Data) and the WorldUpdate oneof.
func generateGoWorldProto(outDir, golemImport, goPackage string, proto schema.ProtoTemplateData) error {
	var b strings.Builder
	w := func(format string, args ...any) { fmt.Fprintf(&b, format+"\n", args...) }

	// Determine which extra imports are needed based on source: declarations.
	needOS := false
	needFilepath := false
	needTiled := false
	needLDtk := false
	needYAML := false
	for _, wt := range proto.WorldTypes {
		if wt.Source == nil {
			continue
		}
		needOS = true
		if wt.Source.URLPrefix != "" {
			needFilepath = true
		}
		switch wt.Source.Format {
		case "tiled":
			needTiled = true
		case "ldtk":
			needLDtk = true
		case "catalog":
			needYAML = true
		}
	}

	w("// GENERATED by Golem Engine — do not edit.")
	w("package %s", goPackage)
	w("")
	w("import (")
	w("\t\"fmt\"")
	if needOS {
		w("\t\"os\"")
	}
	if needFilepath {
		w("\t\"path/filepath\"")
	}
	if needYAML {
		w("\t\"strconv\"")
	}
	w("")
	w("\t\"%s/pb\"", golemImport)
	if needTiled {
		w("\t\"%s/tiled\"", golemImport)
	}
	if needLDtk {
		w("\t\"%s/ldtk\"", golemImport)
	}
	if needYAML {
		w("\t\"gopkg.in/yaml.v3\"")
	}
	w(")")

	for i, wt := range proto.WorldTypes {
		writeGoWorldDataStruct(&b, wt, proto.WorldUpdateFields[i])
		if wt.Source != nil {
			customTypesMap := make(map[string]schema.CustomTypeData, len(proto.CustomTypes))
			for _, ct := range proto.CustomTypes {
				customTypesMap[ct.Name] = ct
			}
			writeGoWorldLoader(&b, wt, customTypesMap)
		}
	}

	writeGoWorldUpdate(&b, proto)

	outPath := filepath.Join(outDir, "world_pb.go")
	if err := os.WriteFile(outPath, []byte(b.String()), 0o644); err != nil {
		return err
	}
	cmd := exec.Command("gofmt", "-w", outPath)
	if out, err := cmd.CombinedOutput(); err != nil {
		return fmt.Errorf("gofmt %s: %w\n%s", outPath, err, out)
	}
	fmt.Printf("  wrote %s\n", outPath)
	return nil
}

// writeGoWorldDataStruct emits a world data struct with Marshal, MarshalUpdate,
// Unmarshal, and WorldName. wuf.Tag is the oneof tag inside WorldUpdate.
func writeGoWorldDataStruct(b *strings.Builder, wt schema.WorldTypeData, wuf schema.WorldUpdateField) {
	w := func(f string, a ...any) { fmt.Fprintf(b, f+"\n", a...) }

	w("")
	w("// %s holds static world data for %q.", wt.DataName, wt.Name)
	w("type %s struct {", wt.DataName)
	for _, f := range wt.Fields {
		w("\t%s %s", f.GoName, f.GoType)
	}
	w("}")

	w("")
	w("// WorldName returns the world type identifier.")
	w("func (m *%s) WorldName() string { return %q }", wt.DataName, wt.Name)

	// Marshal — raw inner bytes
	w("")
	w("// Marshal encodes the data to protobuf wire format (inner bytes only).")
	w("func (m *%s) Marshal() []byte {", wt.DataName)
	w("\tw := &pb.Writer{}")
	for _, f := range wt.Fields {
		if f.IsRepeated || f.IsMap {
			writeGoWorldFieldCollectionMarshal(b, f)
		} else {
			writeGoFieldEncode(b, f.GoName, f.ProtoType, f.ProtoTag, false)
		}
	}
	w("\treturn w.Finish()")
	w("}")

	// MarshalUpdate — wrapped in WorldUpdate oneof
	w("")
	w("// MarshalUpdate encodes the data wrapped in a WorldUpdate oneof envelope.")
	w("func (m *%s) MarshalUpdate() ([]byte, error) {", wt.DataName)
	w("\tinner := m.Marshal()")
	w("\tw := &pb.Writer{}")
	w("\tw.Tag(%d, 2).Bytes(inner)", wuf.Tag)
	w("\treturn w.Finish(), nil")
	w("}")

	// Unmarshal
	w("")
	w("// Unmarshal decodes the data from protobuf wire format.")
	w("func (m *%s) Unmarshal(data []byte) (retErr error) {", wt.DataName)
	w("\tdefer func() { if r := recover(); r != nil { retErr = fmt.Errorf(\"pb: decode %s: %%v\", r) } }()", wt.DataName)
	w("\trd := pb.NewReader(data)")
	w("\tfor !rd.Done() {")
	w("\t\tfield, wire := rd.Tag()")
	w("\t\tswitch field {")
	for _, f := range wt.Fields {
		if f.IsRepeated || f.IsMap {
			writeGoWorldFieldCollectionUnmarshal(b, f)
		} else {
			w("\t\tcase %d: m.%s = rd.%s()", f.ProtoTag, f.GoName, goWriterMethod[f.ProtoType])
		}
	}
	w("\t\tdefault: rd.Skip(wire)")
	w("\t\t}")
	w("\t}")
	w("\treturn nil")
	w("}")
}

// writeGoWorldFieldCollectionMarshal emits marshal code for a collection WorldFieldInfo.
func writeGoWorldFieldCollectionMarshal(b *strings.Builder, f schema.WorldFieldInfo) {
	w := func(fmt_ string, a ...any) { fmt.Fprintf(b, fmt_+"\n", a...) }
	tag := f.ProtoTag
	if f.IsRepeated {
		w("\tfor _, _elem := range m.%s {", f.GoName)
		if f.ElemIsCustom {
			fmt.Fprintf(b, "\t\tw.Tag(%d, 2).Bytes(_elem.Marshal())\n", tag)
		} else {
			fmt.Fprintf(b, "{ _inner := &pb.Writer{}; _inner.%s(_elem); w.Tag(%d, 2).Bytes(_inner.Finish()) }\n",
				goWriterMethod[f.ElemProtoType], tag)
		}
		w("\t}")
	} else { // IsMap
		w("\tfor _k, _v := range m.%s {", f.GoName)
		keyWT := protoWireType[f.MapKeyProtoType]
		keyWM := goWriterMethod[f.MapKeyProtoType]
		w("\t\t_kv := &pb.Writer{}")
		w("\t\t_kv.Tag(1, %d).%s(_k)", keyWT, keyWM)
		if f.ElemIsCustom {
			w("\t\t_kv.Tag(2, 2).Bytes(_v.Marshal())")
		} else {
			valWT := protoWireType[f.ElemProtoType]
			valWM := goWriterMethod[f.ElemProtoType]
			w("\t\t_kv.Tag(2, %d).%s(_v)", valWT, valWM)
		}
		w("\t\tw.Tag(%d, 2).Bytes(_kv.Finish())", tag)
		w("\t}")
	}
}

// writeGoWorldFieldCollectionUnmarshal emits unmarshal case code for a collection WorldFieldInfo.
func writeGoWorldFieldCollectionUnmarshal(b *strings.Builder, f schema.WorldFieldInfo) {
	w := func(fmt_ string, a ...any) { fmt.Fprintf(b, fmt_+"\n", a...) }
	w("\t\tcase %d:", f.ProtoTag)
	if f.IsRepeated {
		w("\t\t\t_b := rd.Bytes()")
		w("\t\t\tif len(_b) > 0 {")
		if f.ElemIsCustom {
			w("\t\t\t\tvar _elem %s", f.ElemProtoType)
			w("\t\t\t\tif _err := _elem.Unmarshal(_b); _err != nil { return _err }")
			w("\t\t\t\tm.%s = append(m.%s, _elem)", f.GoName, f.GoName)
		} else {
			w("\t\t\t\t_inner := pb.NewReader(_b)")
			w("\t\t\t\tm.%s = append(m.%s, _inner.%s())", f.GoName, f.GoName, goWriterMethod[f.ElemProtoType])
		}
		w("\t\t\t}")
	} else { // IsMap
		w("\t\t\t_b := rd.Bytes()")
		w("\t\t\tif len(_b) > 0 {")
		w("\t\t\t\t_rd := pb.NewReader(_b)")
		w("\t\t\t\tvar _k %s", f.MapKeyGoType)
		w("\t\t\t\tvar _v %s", f.ElemGoType)
		w("\t\t\t\tfor !_rd.Done() {")
		w("\t\t\t\t\t_f, _ := _rd.Tag()")
		w("\t\t\t\t\tswitch _f {")
		w("\t\t\t\t\tcase 1: _k = _rd.%s()", goWriterMethod[f.MapKeyProtoType])
		if f.ElemIsCustom {
			w("\t\t\t\t\tcase 2:")
			w("\t\t\t\t\t\tif _err := _v.Unmarshal(_rd.Bytes()); _err != nil { return _err }")
		} else {
			w("\t\t\t\t\tcase 2: _v = _rd.%s()", goWriterMethod[f.ElemProtoType])
		}
		w("\t\t\t\t\t}")
		w("\t\t\t\t}")
		w("\t\t\t\tif m.%s == nil { m.%s = %s{} }", f.GoName, f.GoName, f.GoType)
		w("\t\t\t\tm.%s[_k] = _v", f.GoName)
		w("\t\t\t} else {")
		w("\t\t\t\tif m.%s == nil { m.%s = %s{} }", f.GoName, f.GoName, f.GoType)
		w("\t\t\t}")
	}
}

// writeGoWorldLoader emits a LoadXxxData(path string) helper for world types
// that have a source: block. For Tiled sources, custom properties are extracted
// from Map.Properties; for LDtk sources, from the first level's FieldInstances.
// For catalog sources, entries are read from a YAML sequence and populated into
// a list or dict field. When url_prefix is set, MapUrl is populated and raw
// bytes are not embedded; otherwise TileData receives the full raw file contents.
func writeGoWorldLoader(b *strings.Builder, wt schema.WorldTypeData, customTypes map[string]schema.CustomTypeData) {
	w := func(f string, a ...any) { fmt.Fprintf(b, f+"\n", a...) }
	src := wt.Source

	if src.Format == "catalog" {
		writeGoWorldCatalogLoader(b, wt, customTypes)
		return
	}

	w("")
	w("// Load%s reads the %s map file at path, populates extracted custom", wt.DataName, src.Format)
	w("// properties, and returns a %s ready to store in the world.Store.", wt.DataName)
	w("func Load%s(path string) (*%s, error) {", wt.DataName, wt.DataName)
	w("\traw, err := os.ReadFile(path)")
	w("\tif err != nil { return nil, err }")

	switch src.Format {
	case "tiled":
		w("\tm, err := tiled.Parse(raw)")
		w("\tif err != nil { return nil, err }")
	case "ldtk":
		w("\tproj, err := ldtk.Parse(raw)")
		w("\tif err != nil { return nil, err }")
	}

	w("\td := &%s{}", wt.DataName)

	// Set the synthetic tile_data or map_url field.
	if src.URLPrefix != "" {
		w("\td.MapUrl = %q + filepath.Base(path)", src.URLPrefix)
	} else {
		w("\td.TileData = raw")
	}

	// Emit property extraction for each declared extract field.
	for _, ex := range src.Extract {
		goName := schema.SnakeToPascal(ex.Name)
		switch src.Format {
		case "tiled":
			w("\tif p, ok := m.PropertyByName(%q); ok {", ex.Name)
			w("\t\td.%s = %s", goName, tiledPropertyConvert(ex.Type, "p"))
			w("\t}")
		case "ldtk":
			w("\tlevels := proj.AllLevels()")
			w("\tif len(levels) > 0 {")
			w("\t\tif f := levels[0].FieldByIdentifier(%q); f != nil {", ex.Name)
			w("\t\t\td.%s = %s", goName, ldtkFieldConvert(ex.Type, "f"))
			w("\t\t}")
			w("\t}")
		}
	}

	w("\treturn d, nil")
	w("}")
}

// writeGoWorldCatalogLoader emits a LoadXxxData(path string) helper for catalog
// world types. It reads a YAML sequence, converts each entry map to the custom
// type struct, and appends to a list or inserts into a map.
func writeGoWorldCatalogLoader(b *strings.Builder, wt schema.WorldTypeData, customTypes map[string]schema.CustomTypeData) {
	w := func(f string, a ...any) { fmt.Fprintf(b, f+"\n", a...) }
	src := wt.Source

	ct := customTypes[src.Type]

	w("")
	w("// Load%s reads the catalog YAML at path and returns a %s ready to store in the world.Store.", wt.DataName, wt.DataName)
	w("func Load%s(path string) (*%s, error) {", wt.DataName, wt.DataName)
	w("\traw, err := os.ReadFile(path)")
	w("\tif err != nil { return nil, err }")
	w("\tvar _rows []map[string]any")
	w("\tif err := yaml.Unmarshal(raw, &_rows); err != nil { return nil, err }")
	w("\td := &%s{}", wt.DataName)
	if src.Key != "" {
		// Determine key Go type for make()
		keyGoType := ""
		for _, cf := range ct.Fields {
			if cf.SnakeName == src.Key {
				keyGoType = cf.GoType
				break
			}
		}
		w("\td.Items = make(map[%s]%s, len(_rows))", keyGoType, src.Type)
	}
	w("\tfor _, _row := range _rows {")
	w("\t\t_elem := %s{}", src.Type)
	for _, cf := range ct.Fields {
		w("\t\tif _fv, _ok := _row[%q]; _ok {", cf.SnakeName)
		w("\t\t\t%s", catalogYAMLConvert(cf.ProtoType, "_elem."+cf.GoName, "_fv"))
		w("\t\t}")
	}
	if src.Key == "" {
		w("\t\td.Items = append(d.Items, _elem)")
	} else {
		keyGoName := schema.SnakeToPascal(src.Key)
		w("\t\td.Items[_elem.%s] = _elem", keyGoName)
	}
	w("\t}")
	w("\treturn d, nil")
	w("}")
}

// catalogYAMLConvert returns a Go statement that reads a YAML-decoded any value
// and assigns it to the target variable with appropriate type conversion.
func catalogYAMLConvert(protoType, target, src string) string {
	switch protoType {
	case "string":
		return fmt.Sprintf("if _s, _ok := %s.(string); _ok { %s = _s }", src, target)
	case "bool":
		return fmt.Sprintf("if _bv, _ok := %s.(bool); _ok { %s = _bv }", src, target)
	case "double":
		return fmt.Sprintf("switch _tv := %s.(type) { case float64: %s = _tv; case int: %s = float64(_tv); case string: if _n, _e := strconv.ParseFloat(_tv, 64); _e == nil { %s = _n } }", src, target, target, target)
	case "float":
		return fmt.Sprintf("switch _tv := %s.(type) { case float64: %s = float32(_tv); case int: %s = float32(_tv); case string: if _n, _e := strconv.ParseFloat(_tv, 32); _e == nil { %s = float32(_n) } }", src, target, target, target)
	case "int32", "sint32":
		return fmt.Sprintf("switch _tv := %s.(type) { case int: %s = int32(_tv); case int64: %s = int32(_tv); case float64: %s = int32(_tv); case string: if _n, _e := strconv.ParseInt(_tv, 10, 32); _e == nil { %s = int32(_n) } }", src, target, target, target, target)
	case "int64", "sint64":
		return fmt.Sprintf("switch _tv := %s.(type) { case int: %s = int64(_tv); case int64: %s = _tv; case float64: %s = int64(_tv); case string: if _n, _e := strconv.ParseInt(_tv, 10, 64); _e == nil { %s = _n } }", src, target, target, target, target)
	case "uint32":
		return fmt.Sprintf("switch _tv := %s.(type) { case int: %s = uint32(_tv); case int64: %s = uint32(_tv); case float64: %s = uint32(_tv); case string: if _n, _e := strconv.ParseUint(_tv, 10, 32); _e == nil { %s = uint32(_n) } }", src, target, target, target, target)
	case "uint64":
		return fmt.Sprintf("switch _tv := %s.(type) { case int: %s = uint64(_tv); case int64: %s = uint64(_tv); case float64: %s = uint64(_tv); case string: if _n, _e := strconv.ParseUint(_tv, 10, 64); _e == nil { %s = _n } }", src, target, target, target, target)
	default:
		return fmt.Sprintf("_ = %s // unsupported catalog field type %s", src, protoType)
	}
}

