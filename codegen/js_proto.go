// protoWireType maps protobuf scalar type names to their wire type numbers.
var protoWireType = map[string]int{
	"double": 1, // 64-bit
	"float":  5, // 32-bit
	"int32":  0, // varint
	"int64":  0,
	"uint32": 0,
	"uint64": 0,
	"sint32": 0,
	"sint64": 0,
	"bool":   0,
	"string": 2, // length-delimited
	"bytes":  2,
}

